package bean;

public class Maker {

	
	private int trap;
	private int basket;
	private int woodHouse;
	private int hunterHouse;
	public int getTrap() {
		return trap;
	}
	public void setTrap(int trap) {
		this.trap = trap;
	}
	public int getBasket() {
		return basket;
	}
	public void setBasket(int basket) {
		this.basket = basket;
	}
	public int getWoodHouse() {
		return woodHouse;
	}
	public void setWoodHouse(int woodHouse) {
		this.woodHouse = woodHouse;
	}
	public int getHunterHouse() {
		return hunterHouse;
	}
	public void setHunterHouse(int hunterHouse) {
		this.hunterHouse = hunterHouse;
	}
	
	
	
}
