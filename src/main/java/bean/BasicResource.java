package bean;

public class BasicResource {

	
	private int wood;
	private int iron;
	private int wool;
	private int stone;
	private int human;
	private int meat;
	private int teeth;
	private int scales;
	private int bait;
	
	public int getWood() {
		return wood;
	}
	public void setWood(int wood) {
		this.wood = wood;
	}
	public int getIron() {
		return iron;
	}
	public void setIron(int iron) {
		this.iron = iron;
	}
	public int getWool() {
		return wool;
	}
	public void setWool(int wool) {
		this.wool = wool;
	}
	public int getStone() {
		return stone;
	}
	public void setStone(int stone) {
		this.stone = stone;
	}
	public int getHuman() {
		return human;
	}
	public void setHuman(int human) {
		this.human = human;
	}
	public int getMeat() {
		return meat;
	}
	public void setMeat(int meat) {
		this.meat = meat;
	}
	public int getTeeth() {
		return teeth;
	}
	public void setTeeth(int teeth) {
		this.teeth = teeth;
	}
	public int getScales() {
		return scales;
	}
	public void setScales(int scales) {
		this.scales = scales;
	}
	public int getBait() {
		return bait;
	}
	public void setBait(int bait) {
		this.bait = bait;
	}
	
	
	
}
